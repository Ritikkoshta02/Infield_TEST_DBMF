def shift_footprint(footprint, x, y):
    return [
        ((a[0] + x, a[1] + y), (b[0] + x, b[1] + y))
        for a, b in footprint
    ]




FOOTPRINT = [
  ((6, 1), (6, 2)), ((6, 2), (6, 3)), ((6, 3), (6, 4)), 
    ((6, 4), (6, 5)), ((6, 5), (6, 6)), ((6, 6), (6, 7)), 
    ((6, 7), (6, 8)), ((6, 8), (6, 9)), ((6, 9), (6, 10)), 
    ((6, 4), (7, 4)), ((7, 4), (8, 4)), ((8, 4), (9, 4)), 
    ((9, 4), (9, 5)), ((9, 5), (9, 6)), ((9, 6), (9, 7)), 
    ((6, 7), (7, 7)), ((7, 7), (8, 7)), ((8, 7), (9, 7)), 
    ((9, 7), (10, 7)), ((1, 4), (2, 4)), ((2, 4), (3, 4)), 
    ((3, 4), (4, 4)), ((4, 4), (5, 4)), ((5, 4), (6, 4)), 
    ((2, 7), (3, 7)), ((3, 7), (4, 7)), ((4, 7), (5, 7)), 
    ((5, 7), (6, 7)), ((2, 4), (2, 5)), ((2, 5), (2, 6)), 
    ((2, 6), (2, 7))

]

# Example usage
x, y = 20, 10  # Shift values
new_footprint = shift_footprint(FOOTPRINT, x, y)
print(new_footprint)
