def merge_obstacles(main_list, secondary_list, x):
    # Ensure x is within the valid range
    if x >= len(main_list):
        main_list.extend(secondary_list)
        return main_list
    
    for i, sec_elem in enumerate(secondary_list):
        target_index = x + i
        if target_index < len(main_list):
            main_list[target_index].extend(sec_elem)
        else:
            main_list.append(sec_elem)
    
    return main_list

